public class Switch {
    public static void main(String[] args) {
        int a = 3;
        switch(a){
            case 1:
                System.out.println("Sunday");
                break;
            case 2:
                System.out.println("monday");
                break;
            case 7:
                System.out.println("saturday");
                break;
            default:
                System.out.println(" hii ");
        }
    }
}
